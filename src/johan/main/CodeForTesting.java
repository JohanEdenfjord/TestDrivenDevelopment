package johan.main;

public class CodeForTesting {

    public float lowestNumber(float a, float b){
        return Math.min(a,b);
    }

    public int lowestNumber(int a, int b){
        return Math.min(a,b);
    }

    public double lowestNumber(double a, double b){
        return Math.min(a,b);
    }

    public long lowestNumber(long a, long b){
        return Math.min(a,b);
    }

    public float highestNumber(float a, float b) { return Math.max(a,b); }

    public int highestNumber(int a, int b) { return Math.max(a,b); }

    public double highestNumber(double a, double b) { return Math.max(a,b); }

    public long highestNumber(long a, long b) { return Math.max(a,b); }

    public double powerOf(double a, double b) { return Math.pow(a,b); }

    public int addOne(int a) { return a + 1; }

    public long addOne(long a) { return a + 1L; }

    public int subtractOne(int a) { return a - 1; }

    public long subtractOne(long a) { return a - 1L; }

    public double squareRoot(double a) { return Math.sqrt(a); }

    public double cubicRoot(double a) { return Math.cbrt(a); }


}




